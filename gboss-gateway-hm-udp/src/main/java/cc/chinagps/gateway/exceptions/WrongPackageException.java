package cc.chinagps.gateway.exceptions;

public class WrongPackageException extends Exception {
	private static final long serialVersionUID = 1L;
	
	public WrongPackageException() {
		super();
	}

	public WrongPackageException(String message) {
		super(message);
	}

	public WrongPackageException(String message, Throwable cause) {
		super(message, cause);
	}

	public WrongPackageException(Throwable cause) {
		super(cause);
	}
}