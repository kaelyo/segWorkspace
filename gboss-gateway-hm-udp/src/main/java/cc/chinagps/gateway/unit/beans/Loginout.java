package cc.chinagps.gateway.unit.beans;

public class Loginout {
	public static int LOGOUT = 0;
	public static int LOGIN = 1;
	public static int CHANGE_LOGIN = 2;
	
	private int isLogin;
	
	private long loginTime;
	
	private long logoutTime;
	
	private String unitVersion;
	
	private String callLetter;

	public String getCallLetter() {
		return callLetter;
	}

	public void setCallLetter(String callLetter) {
		this.callLetter = callLetter;
	}

	public String getUnitVersion() {
		return unitVersion;
	}

	public void setUnitVersion(String unitVersion) {
		this.unitVersion = unitVersion;
	}

	public int getIsLogin() {
		return isLogin;
	}

	public void setIsLogin(int isLogin) {
		this.isLogin = isLogin;
	}

	public long getLoginTime() {
		return loginTime;
	}

	public void setLoginTime(long loginTime) {
		this.loginTime = loginTime;
	}

	public long getLogoutTime() {
		return logoutTime;
	}

	public void setLogoutTime(long logoutTime) {
		this.logoutTime = logoutTime;
	}
}