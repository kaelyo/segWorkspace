package cc.chinagps.gateway.unit.pengaoda.upload.beans;

public class PengAoDaBaseStation {
	private int mcc;
	
	private int mnc;
	
	private int lac;
	
	private int cellId;

	public int getCellId() {
		return cellId;
	}

	public void setCellId(int cellId) {
		this.cellId = cellId;
	}

	public int getLac() {
		return lac;
	}

	public void setLac(int lac) {
		this.lac = lac;
	}

	public int getMcc() {
		return mcc;
	}

	public void setMcc(int mcc) {
		this.mcc = mcc;
	}

	public int getMnc() {
		return mnc;
	}

	public void setMnc(int mnc) {
		this.mnc = mnc;
	}
	
	@Override
	public String toString() {
		StringBuilder sb = new StringBuilder();
		sb.append("{mcc:").append(mcc);
		sb.append(", mnc:").append(mnc);
		sb.append(", lac:").append(lac);
		sb.append(", cellId:").append(cellId);
		sb.append("}");
		
		return sb.toString();
	}
}