package cc.chinagps.gateway.unit.leopaard.upload.bean;

public class LeopaardTerminalInfo {
	private String softwareVersion;
	private String hardwareVersion;
	private String vin;
	private String barCode;
	private long manufactureTime;
	private long softwareGenTime;
	private long lastUpgradeTime;

	public String getSoftwareVersion() {
		return softwareVersion;
	}

	public void setSoftwareVersion(String softwareVersion) {
		this.softwareVersion = softwareVersion;
	}

	public String getHardwareVersion() {
		return hardwareVersion;
	}

	public void setHardwareVersion(String hardwareVersion) {
		this.hardwareVersion = hardwareVersion;
	}

	public String getVin() {
		return vin;
	}

	public void setVin(String vin) {
		this.vin = vin;
	}

	public String getBarCode() {
		return barCode;
	}

	public void setBarCode(String barCode) {
		this.barCode = barCode;
	}

	public long getManufactureTime() {
		return manufactureTime;
	}

	public void setManufactureTime(long manufactureTime) {
		this.manufactureTime = manufactureTime;
	}

	public long getSoftwareGenTime() {
		return softwareGenTime;
	}

	public void setSoftwareGenTime(long softwareGenTime) {
		this.softwareGenTime = softwareGenTime;
	}

	public long getLastUpgradeTime() {
		return lastUpgradeTime;
	}

	public void setLastUpgradeTime(long lastUpgradeTime) {
		this.lastUpgradeTime = lastUpgradeTime;
	}

}
