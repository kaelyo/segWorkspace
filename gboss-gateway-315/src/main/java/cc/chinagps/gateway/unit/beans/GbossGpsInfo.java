package cc.chinagps.gateway.unit.beans;

import cc.chinagps.gateway.buff.GBossDataBuff.GpsInfo;

public class GbossGpsInfo {
	private GpsInfo gps;
	private GpsInfo.Builder gpsBuilder;
	private int protocolType;

	public GbossGpsInfo(GpsInfo gps, int protocolType) {
		super();
		this.gps = gps;
		this.protocolType = protocolType;
	}
	
	public GbossGpsInfo(GpsInfo.Builder gpsBuilder, int protocolType) {
		super();
		this.gpsBuilder = gpsBuilder;
		this.protocolType = protocolType;
	}
	
	public GbossGpsInfo(GpsInfo.Builder gpsBuilder,GpsInfo gps, int protocolType) {
		super();
		this.gps = gps;
		this.gpsBuilder = gpsBuilder;
		this.protocolType = protocolType;
	}

	public GpsInfo getGps() {
		return gps;
	}

	public void setGps(GpsInfo gps) {
		this.gps = gps;
	}

	public int getProtocolType() {
		return protocolType;
	}

	public void setProtocolType(int protocolType) {
		this.protocolType = protocolType;
	}

	public GpsInfo.Builder getGpsBuilder() {
		return gpsBuilder;
	}

	public void setGpsBuilder(GpsInfo.Builder gpsBuilder) {
		this.gpsBuilder = gpsBuilder;
	}
}
