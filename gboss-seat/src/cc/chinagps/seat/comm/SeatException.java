package cc.chinagps.seat.comm;

/**
 * 座席异常
 * @author Administrator
 *
 */
public class SeatException extends Exception {

	private static final long serialVersionUID = 2805040694793798327L;

	public SeatException() {}

	public SeatException(String message) {
		super(message);
	}

	public SeatException(Throwable cause) {
		super(cause);
	}

	public SeatException(String message, Throwable cause) {
		super(message, cause);
	}

}
