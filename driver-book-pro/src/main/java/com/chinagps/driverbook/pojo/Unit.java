package com.chinagps.driverbook.pojo;

/**
 * 终端POJO
 * 
 * @author Ben
 *
 */
public class Unit extends BaseEntity {
	private static final long serialVersionUID = -6349158797228786429L;

	/** 终端ID */
	private Long unitId;
	/** 客户ID */
	private Long customerId;
	/** 车辆ID */
	private Long vehicleId;
	/** 车载号码 */
	private String callLetter;
	/** 车载号码 */
	private String subcoNo;

	public String getSubcoNo() {
		return subcoNo;
	}

	public void setSubcoNo(String subcoNo) {
		this.subcoNo = subcoNo;
	}

	public Long getUnitId() {
		return unitId;
	}

	public void setUnitId(Long unitId) {
		this.unitId = unitId;
	}

	public Long getCustomerId() {
		return customerId;
	}

	public void setCustomerId(Long customerId) {
		this.customerId = customerId;
	}

	public Long getVehicleId() {
		return vehicleId;
	}

	public void setVehicleId(Long vehicleId) {
		this.vehicleId = vehicleId;
	}

	public String getCallLetter() {
		return callLetter;
	}

	public void setCallLetter(String callLetter) {
		this.callLetter = callLetter;
	}

}
