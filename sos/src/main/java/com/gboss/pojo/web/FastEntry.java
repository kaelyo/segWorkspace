package com.gboss.pojo.web;

import java.util.List;

import com.gboss.pojo.Linkman;

/**
 * 用于快速入网页面传值到Controller的POJO类
 * 
 * @author xiaok
 * 
 */
public class FastEntry {

	private Integer type;// 客户类型
	private Long cust_id;//客户ID
	private String customer_name;// 客户名称
	private Long companyno;//所属机构ID
	private String companycode;//机构code
	private String manager_name;// 销售经理
	private Long manager_id;// 销售经理用户ID
	private String number_plate;// 车牌号码
	private String call_letter;// 车载号码
	private String server_pwd;// 服务密码
	private String code;// 终端型号
	private Long product_id;// 商品ID
	private Integer mode;// 通信模式
	private String time;// 安装时间
	private List<Linkman> custphones;// 客户多个联系电话

	public Integer getType() {
		return type;
	}

	public void setType(Integer type) {
		this.type = type;
	}
	
	public Long getCust_id() {
		return cust_id;
	}

	public void setCust_id(Long cust_id) {
		this.cust_id = cust_id;
	}

	public String getCustomer_name() {
		return customer_name;
	}

	public void setCustomer_name(String customer_name) {
		this.customer_name = customer_name;
	}
	
	public Long getCompanyno() {
		return companyno;
	}

	public void setCompanyno(Long companyno) {
		this.companyno = companyno;
	}

	public String getCompanycode() {
		return companycode;
	}

	public void setCompanycode(String companycode) {
		this.companycode = companycode;
	}

	public String getManager_name() {
		return manager_name;
	}

	public void setManager_name(String manager_name) {
		this.manager_name = manager_name;
	}

	public Long getManager_id() {
		return manager_id;
	}

	public void setManager_id(Long manager_id) {
		this.manager_id = manager_id;
	}

	public String getNumber_plate() {
		return number_plate;
	}

	public void setNumber_plate(String number_plate) {
		this.number_plate = number_plate;
	}

	public String getCall_letter() {
		return call_letter;
	}

	public void setCall_letter(String call_letter) {
		this.call_letter = call_letter;
	}

	public String getServer_pwd() {
		return server_pwd;
	}

	public void setServer_pwd(String server_pwd) {
		this.server_pwd = server_pwd;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public Integer getMode() {
		return mode;
	}

	public void setMode(Integer mode) {
		this.mode = mode;
	}

	public String getTime() {
		return time;
	}

	public void setTime(String time) {
		this.time = time;
	}

	public List<Linkman> getCustphones() {
		return custphones;
	}

	public void setCustphones(List<Linkman> custphones) {
		this.custphones = custphones;
	}

	public Long getProduct_id() {
		return product_id;
	}

	public void setProduct_id(Long product_id) {
		this.product_id = product_id;
	}

}
