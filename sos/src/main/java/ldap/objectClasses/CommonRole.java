package ldap.objectClasses;

import java.util.List;

public class CommonRole {
	
	private String dn;
	
	private String roleid;	//角色id
	
	private String rolename;//角色名称
	
	private String systemid;//归属系统id
	
	private String remark;	//备注
	
	private String companyno;	//分公司id 1:为所有公司操作员能看到， 其他为该公司操作员可看到
	
	private String isadmin;	//是否具有数据权限
	
	private List<String> moduleids;//拥有模块id

	public String getDn() {
		return dn;
	}

	public void setDn(String dn) {
		this.dn = dn;
	}

	public String getRoleid() {
		return roleid;
	}

	public void setRoleid(String roleid) {
		this.roleid = roleid;
	}

	public String getRolename() {
		return rolename;
	}

	public void setRolename(String rolename) {
		this.rolename = rolename;
	}

	public String getSystemid() {
		return systemid;
	}

	public void setSystemid(String systemid) {
		this.systemid = systemid;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

	public String getCompanyno() {
		return companyno;
	}

	public void setCompanyno(String companyno) {
		this.companyno = companyno;
	}

	public String getIsadmin() {
		return isadmin;
	}

	public void setIsadmin(String isadmin) {
		this.isadmin = isadmin;
	}

	public List<String> getModuleids() {
		return moduleids;
	}

	public void setModuleids(List<String> moduleids) {
		this.moduleids = moduleids;
	}

}
