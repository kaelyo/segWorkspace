package com.gboss.comm;

import org.springframework.beans.factory.annotation.Value;
import org.springframework.stereotype.Service;

@Service("SystemConfig")
public class SystemConfig {

	@Value("${sysnDadaUrl}")
    private String sysnDadaUrl;
	
	@Value("${sysnDadaPort}")
    private String sysnDadaPort;
	
	@Value("${sysnSeries}")
    private String sysnSeries;
	
	@Value("${sysnModel}")
    private String sysnModel;
	
	@Value("${ssssList}")
    private String ssssList;
	
	@Value("${insuracerList}")
    private String insuracerList;
	
	@Value("${packList}")
    private String packList;
	
	@Value("${info}")
    private String info;
	
	@Value("${bindInfo}")
    private String bandInfo;
	
	@Value("${waittime}")
	private String waittime;
	
	@Value("${dalaytime}")
	private String dalaytime;
	
	@Value("${ahead_of_time}")
	private String aheadOfTime;
	
	@Value("${account}")
	private String account;
	
	@Value("${ips}")
	private String ips;
	
	public String getIps() {
		return ips;
	}

	public void setIps(String ips) {
		this.ips = ips;
	}

	public String getAccount() {
		return account;
	}

	public void setAccount(String account) {
		this.account = account;
	}

	public String getDalaytime() {
		return dalaytime;
	}

	public void setDalaytime(String dalaytime) {
		this.dalaytime = dalaytime;
	}

	public String getWaittime() {
		return waittime;
	}

	public void setWaittime(String waittime) {
		this.waittime = waittime;
	}

	public String getBandInfo() {
		return bandInfo;
	}

	public void setBandInfo(String bandInfo) {
		this.bandInfo = bandInfo;
	}
	
	public String getSysnDadaUrl() {
		return sysnDadaUrl;
	}

	public void setSysnDadaUrl(String sysnDadaUrl) {
		this.sysnDadaUrl = sysnDadaUrl;
	}

	public String getSysnDadaPort() {
		return sysnDadaPort;
	}

	public void setSysnDadaPort(String sysnDadaPort) {
		this.sysnDadaPort = sysnDadaPort;
	}

	public String getSysnSeries() {
		return sysnSeries;
	}

	public void setSysnSeries(String sysnSeries) {
		this.sysnSeries = sysnSeries;
	}

	public String getSysnModel() {
		return sysnModel;
	}

	public void setSysnModel(String sysnModel) {
		this.sysnModel = sysnModel;
	}

	public String getSsssList() {
		return ssssList;
	}

	public void setSsssList(String ssssList) {
		this.ssssList = ssssList;
	}

	public String getInsuracerList() {
		return insuracerList;
	}

	public void setInsuracerList(String insuracerList) {
		this.insuracerList = insuracerList;
	}

	public String getPackList() {
		return packList;
	}

	public void setPackList(String packList) {
		this.packList = packList;
	}

	public String getInfo() {
		return info;
	}

	public void setInfo(String info) {
		this.info = info;
	}

	public String getAheadOfTime() {
		return aheadOfTime;
	}

	public void setAheadOfTime(String aheadOfTime) {
		this.aheadOfTime = aheadOfTime;
	}
	
}
